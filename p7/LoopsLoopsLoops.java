public class LoopsLoopsLoops {
    public static void main(String[] args) {
        System.out.println("Below is generated using while loop: ");
        int oneto20 = 1;
        while (oneto20 <= 20) {
            System.out.print(oneto20 + " ");
            oneto20++;
        }  
        System.out.println("\n");

        System.out.println("below is generated using dowhile loops: ");
        int decreaseno = 50;
        do {
            System.out.print(decreaseno+" ");
            decreaseno--;
        } while (decreaseno >= 35);
        System.out.print("\n \n");
     

        System.out.println("below is generated using for loops: ");
        for (char characters = 'a'; characters <= 'z'; characters++) {
            System.out.print(characters + " ");
            
        } 
        
        





    }
}
